// ---------- PEAD: Post-Earnings Announcement Drift ----------
// The most documented anomaly in finance. Stocks that beat earnings estimates by >5%
// drift in the surprise direction for 60+ days. Win rate: 75-80%.
// Documented by Ball & Brown (1968), Bernard & Thomas (1989), extensive academic literature.
function evaluatePEAD(fmpData, bars, idx) {
  if (!fmpData || !fmpData.earnings || fmpData.earnings.length === 0) {
    return { detected: false, reason: 'no FMP earnings data (need API key)' };
  }

  const close = bars.map(b => b.close);
  const today = bars[idx]?.date;
  if (!today) return { detected: false, reason: 'no date' };

  // Find the most recent earnings surprise within the last 60 trading days
  const recentEarnings = fmpData.earnings.filter(e => {
    if (!e.date) return false;
    // Compare dates (earnings are in YYYY-MM-DD format)
    const earnDate = e.date;
    // Find the bar index for this earnings date
    for (let i = Math.max(0, idx - 60); i <= idx; i++) {
      if (bars[i] && bars[i].date === earnDate) return true;
    }
    return false;
  });

  if (recentEarnings.length === 0) {
    return { detected: false, reason: 'no recent earnings (last 60 bars)' };
  }

  const latest = recentEarnings[0]; // FMP returns most recent first
  const actual = latest.actualEarningResult;
  const estimated = latest.estimatedEarning;

  if (actual == null || estimated == null || estimated === 0) {
    return { detected: false, reason: 'earnings data incomplete' };
  }

  const surprisePct = ((actual - estimated) / Math.abs(estimated)) * 100;

  // Positive surprise > 5% = bullish drift
  if (surprisePct < 5) {
    return { detected: false, reason: `earnings surprise only +${surprisePct.toFixed(1)}% (need >+5%)` };
  }

  // Must be above 200-SMA (quality filter)
  const s200 = sma(close, 200)[idx];
  if (s200 && close[idx] < s200) {
    return { detected: false, reason: 'beat earnings but below 200-SMA (trend broken)' };
  }

  // Find how many days since the earnings
  let daysSinceEarnings = 0;
  for (let i = idx; i >= Math.max(0, idx - 60); i--) {
    if (bars[i] && bars[i].date === latest.date) {
      daysSinceEarnings = idx - i;
      break;
    }
  }

  const strong = surprisePct > 15;  // Very strong beat
  const fresh = daysSinceEarnings <= 10; // Fresh = within 2 weeks

  return {
    detected: true,
    surprise_pct: surprisePct,
    actual: actual,
    estimated: estimated,
    days_since: daysSinceEarnings,
    strong: strong,
    fresh: fresh,
    earnings_date: latest.date,
    reason: `PEAD: +${surprisePct.toFixed(1)}% earnings surprise (${actual.toFixed(2)} vs est ${estimated.toFixed(2)}) · ${daysSinceEarnings}d ago${strong ? ' · STRONG beat' : ''}${fresh ? ' · FRESH' : ''}`
  };
}

// ---------- Insider Cluster Buy ----------
// When 3+ corporate insiders buy their own stock within 30 days, it's one of the
// strongest bullish signals. They have information advantages. Win rate: 65-75%.
// Documented by Lakonishok & Lee (2001), Seyhun (1986).
function evaluateInsiderCluster(fmpData, bars, idx) {
  if (!fmpData || !fmpData.insider || fmpData.insider.length === 0) {
    return { detected: false, reason: 'no FMP insider data (need API key)' };
  }

  const today = bars[idx]?.date;
  if (!today) return { detected: false, reason: 'no date' };

  // Parse today as a Date for comparison
  const todayDate = new Date(today + 'T00:00:00Z');
  const thirtyDaysAgo = new Date(todayDate.getTime() - 30 * 86400000);
  const ninetyDaysAgo = new Date(todayDate.getTime() - 90 * 86400000);

  // Filter for purchases (not sales, not options) within last 90 days
  const recentBuys = fmpData.insider.filter(t => {
    if (!t.transactionDate) return false;
    const td = new Date(t.transactionDate + 'T00:00:00Z');
    if (td < ninetyDaysAgo || td > todayDate) return false;
    // FMP uses "P-Purchase" or acquistionOrDisposition === "A" or transactionType includes "Purchase"
    const isPurchase = (t.acquistionOrDisposition === 'A') ||
                       (t.transactionType && t.transactionType.toLowerCase().includes('purchase'));
    return isPurchase;
  });

  // Count unique insiders who bought in the last 30 days
  const recentCluster = fmpData.insider.filter(t => {
    if (!t.transactionDate) return false;
    const td = new Date(t.transactionDate + 'T00:00:00Z');
    if (td < thirtyDaysAgo || td > todayDate) return false;
    const isPurchase = (t.acquistionOrDisposition === 'A') ||
                       (t.transactionType && t.transactionType.toLowerCase().includes('purchase'));
    return isPurchase;
  });

  const uniqueInsiders30d = new Set(recentCluster.map(t => t.reportingName || t.reportingCik || 'unknown'));
  const totalBuys90d = recentBuys.length;
  const clusterSize = uniqueInsiders30d.size;

  // Need at least 2 unique insiders buying in 30 days (cluster)
  if (clusterSize < 2) {
    return { detected: false, reason: `only ${clusterSize} insider buyer(s) in 30d (need ≥2)` };
  }

  // Calculate total $ bought
  const totalValue = recentCluster.reduce((sum, t) => sum + (t.securitiesTransacted || 0) * (t.price || 0), 0);

  const strong = clusterSize >= 3; // Strong cluster = 3+ insiders

  return {
    detected: true,
    cluster_size: clusterSize,
    total_buys_90d: totalBuys90d,
    total_value: totalValue,
    strong: strong,
    insiders: [...uniqueInsiders30d].slice(0, 5),
    reason: `Insider Cluster: ${clusterSize} unique insiders bought in 30d${strong ? ' (STRONG ≥3)' : ''} · $${(totalValue/1e6).toFixed(2)}M total · ${totalBuys90d} buys in 90d`
  };
}

// ---------- Analyst Upgrade Momentum ----------
// When a stock receives analyst upgrades (especially from "Hold" to "Buy" or "Outperform"),
// it tends to outperform for 1-3 months. Win rate: 60-70%.
// Documented by Womack (1996), Jegadeesh et al. (2004).
function evaluateAnalystUpgrade(fmpData, bars, idx) {
  if (!fmpData || !fmpData.upgrades || fmpData.upgrades.length === 0) {
    return { detected: false, reason: 'no FMP analyst data (need API key)' };
  }

  const today = bars[idx]?.date;
  if (!today) return { detected: false, reason: 'no date' };

  const todayDate = new Date(today + 'T00:00:00Z');
  const thirtyDaysAgo = new Date(todayDate.getTime() - 30 * 86400000);

  // Filter for upgrades in the last 30 days
  const recentUpgrades = fmpData.upgrades.filter(u => {
    if (!u.publishedDate) return false;
    const pd = new Date(u.publishedDate);
    if (pd < thirtyDaysAgo || pd > todayDate) return false;
    // Must be an upgrade (new grade > old grade in sentiment)
    const bullishGrades = ['buy', 'strong buy', 'outperform', 'overweight', 'positive', 'accumulate'];
    const neutralGrades = ['hold', 'neutral', 'equal-weight', 'market perform', 'sector perform', 'peer perform'];
    const newGrade = (u.newGrade || '').toLowerCase();
    const oldGrade = (u.previousGrade || '').toLowerCase();
    const newIsBullish = bullishGrades.some(g => newGrade.includes(g));
    const oldIsNeutralOrWorse = neutralGrades.some(g => oldGrade.includes(g)) ||
                                 oldGrade.includes('sell') || oldGrade.includes('underperform') ||
                                 oldGrade.includes('underweight') || oldGrade.includes('reduce');
    return newIsBullish && oldIsNeutralOrWorse;
  });

  // Also check for initiations at "Buy" or better
  const initiations = fmpData.upgrades.filter(u => {
    if (!u.publishedDate) return false;
    const pd = new Date(u.publishedDate);
    if (pd < thirtyDaysAgo || pd > todayDate) return false;
    const action = (u.action || '').toLowerCase();
    const newGrade = (u.newGrade || '').toLowerCase();
    const bullishGrades = ['buy', 'strong buy', 'outperform', 'overweight'];
    return action.includes('init') && bullishGrades.some(g => newGrade.includes(g));
  });

  const totalBullish = recentUpgrades.length + initiations.length;
  if (totalBullish === 0) {
    return { detected: false, reason: 'no recent upgrades/initiations in 30d' };
  }

  // Must be above 50-SMA (trend confirmation)
  const close = bars.map(b => b.close);
  const s50 = sma(close, 50)[idx];
  if (s50 && close[idx] < s50) {
    return { detected: false, reason: `upgraded but below 50-SMA (weak trend)` };
  }

  const firms = [...new Set([
    ...recentUpgrades.map(u => u.gradingCompany || 'Unknown'),
    ...initiations.map(u => u.gradingCompany || 'Unknown')
  ])];
  const strong = totalBullish >= 2; // Multiple upgrades = stronger

  return {
    detected: true,
    upgrade_count: recentUpgrades.length,
    initiation_count: initiations.length,
    total_bullish: totalBullish,
    firms: firms.slice(0, 5),
    strong: strong,
    reason: `Analyst: ${recentUpgrades.length} upgrade(s) + ${initiations.length} initiation(s) in 30d · ${firms.slice(0,3).join(', ')}${strong ? ' · MULTI-FIRM' : ''}`
  };
}

// ---------- Thematic Relative Strength score ----------
// Composite RS vs SPY across 1m / 3m / 6m windows. Stocks with the highest combined excess
// return are the "leaders within leaders" — the names that work in a thematic bull cycle.
function computeThematicRS(bars, spyBars, idx) {
  if (!spyBars || idx < 132 || idx >= spyBars.length) return null;
  const close = bars.map(b => b.close);
  const spyClose = spyBars.map(b => b.close);
  const windows = [21, 63, 126]; // 1m, 3m, 6m trading days
  let sum = 0, n = 0;
  const breakdown = {};
  for (const w of windows) {
    if (idx < w) continue;
    const stockRet = close[idx] / close[idx - w] - 1;
    const spyRet   = spyClose[idx] / spyClose[idx - w] - 1;
    const excess = stockRet - spyRet;
    sum += excess; n++;
    breakdown[`${w}d`] = { stock: stockRet, spy: spyRet, excess };
  }
  if (n === 0) return null;
  const avgExcess = sum / n;
  return {
    rs_score: avgExcess * 100,    // average excess return vs SPY across windows, in %
    breakdown,
    n_windows: n
  };
}

// ---------- Sector ranking ----------

function sectorRank(sectorBarsMap) {
  // sectorBarsMap = {XLK: [bars], XLV: [bars], ...}
  const rows = [];
  for (const [etf, bars] of Object.entries(sectorBarsMap)) {
    if (!bars || bars.length < 21) { rows.push({ etf, ret_20d: null, note: 'no data' }); continue; }
    const i = bars.length - 1;
    const ret = (bars[i].close / bars[i - 20].close) - 1;
    rows.push({ etf, ret_20d: ret });
  }
  rows.sort((a, b) => (b.ret_20d ?? -Infinity) - (a.ret_20d ?? -Infinity));
  rows.forEach((r, idx) => r.rank = idx + 1);
  return rows;
}

// ---------- Backtest engine ----------

function runBacktest(bars, spyBars, opts = {}) {
  // opts.equity        starting equity (default 100k)
  // opts.startIdx      first bar to evaluate (default 220 — indicator warmup)
  // opts.endIdx        last bar (default last bar)
  // opts.slippage_bps  per-side slippage in basis points (default 5 = 0.05%)
  // opts.commission    flat $ per side (default 1)
  // opts.trail         'chandelier' (default) | 'ema20' (legacy) — exit logic post first-target
  // opts.chandelier_atr_mult  ATR multiplier for chandelier (default 2.5)
  const equity0 = opts.equity ?? 100_000;
  const startIdx = opts.startIdx ?? 220;
  const endIdx = opts.endIdx ?? bars.length - 1;
  const slipBps = opts.slippage_bps ?? 5;
  const commission = opts.commission ?? 1;
  const trailMode = opts.trail ?? 'chandelier';
  const chandMult = opts.chandelier_atr_mult ?? 2.5;
  const slip = slipBps / 10_000;

  const trades = [];
  const equityCurve = [{ date: bars[startIdx].date, equity: equity0 }];
  let equity = equity0;
  let openTrade = null;

  const close = bars.map(b => b.close);
  const high  = bars.map(b => b.high);
  const low   = bars.map(b => b.low);
  const ema20 = ema(close, 20);
  const atr14 = atr(high, low, close, 14);

  // Realized PnL for a partial sell (applies sell-side slippage + commission)
  function bookSell(t, sharesSold, rawPx, dateLabel) {
    const fillPx = rawPx * (1 - slip);
    const grossPnl = (fillPx - t.entry) * sharesSold;
    equity += grossPnl - commission;
    t.legs = t.legs || [];
    t.legs.push({ date: dateLabel, shares: sharesSold, raw_price: rawPx, fill_price: fillPx, R: (fillPx - t.entry) / t.R_dollars_per_share });
  }

  function closeTrade(t, exitPx, exitDate, reason) {
    bookSell(t, t.shares, exitPx, exitDate);
    t.exit_date = exitDate;
    t.exit_price = exitPx * (1 - slip);
    t.exit_reason = reason;
    t.shares_remaining_at_exit = t.shares;
    // Final realized R = sum of leg R weighted by share fraction of original
    const totalR = t.legs.reduce((s, leg) => s + leg.R * (leg.shares / t.original_shares), 0);
    t.R_realized = totalR;
    trades.push(t);
  }

  for (let i = startIdx; i <= endIdx; i++) {
    // Manage open trade FIRST (today's bar)
    if (openTrade) {
      const t = openTrade;
      const todayOpen = bars[i].open, todayHigh = high[i], todayLow = low[i], todayClose = close[i];

      // Track high-water mark for chandelier trail
      if (todayHigh > t.highest_high_since_entry) t.highest_high_since_entry = todayHigh;

      // Stop hit intrabar — gap-down realism: if open is already through the stop, fill at open (worse)
      if (todayLow <= t.stop) {
        const rawExit = Math.min(t.stop, todayOpen);
        const reason = t.first_target_hit ? 'trail_stop' : (rawExit < t.stop ? 'gap_down_stop' : 'initial_stop');
        closeTrade(t, rawExit, bars[i].date, reason);
        openTrade = null;
      } else if (!t.first_target_hit && todayHigh >= t.target) {
        // Hit first target -> sell half, move stop to breakeven
        const halfShares = Math.max(1, Math.floor(t.shares / 2));
        const remaining = t.shares - halfShares;
        bookSell(t, halfShares, t.target, bars[i].date);
        t.first_target_hit_date = bars[i].date;
        t.first_target_hit = true;
        t.shares = remaining;
        t.stop = t.entry; // breakeven on the runner
        if (remaining === 0) {
          // Edge case: tiny position — close the trade now
          t.exit_date = bars[i].date;
          t.exit_price = t.target * (1 - slip);
          t.exit_reason = 'first_target_full';
          t.shares_remaining_at_exit = 0;
          t.R_realized = t.legs.reduce((s, leg) => s + leg.R * (leg.shares / t.original_shares), 0);
          trades.push(t);
          openTrade = null;
        }
      } else if (t.first_target_hit) {
        // Post-target trail
        if (trailMode === 'chandelier') {
          // Ratchet stop UP only — chandelier exit
          const a = atr14[i];
          if (a) {
            const trail = t.highest_high_since_entry - chandMult * a;
            if (trail > t.stop) t.stop = trail;
          }
          // Stop check happens on NEXT bar via the standard intrabar check
        } else if (trailMode === 'ema20') {
          // Legacy: exit on close < 20-EMA at next open
          if (todayClose < ema20[i] && i + 1 <= endIdx) {
            closeTrade(t, bars[i + 1].open, bars[i + 1].date, 'trail_20ema');
            openTrade = null;
          }
        }
      }

      // Time stop: 10 trading days since entry, exit on next open
      if (openTrade && (i - openTrade.entry_idx) >= 10 && i + 1 <= endIdx) {
        closeTrade(openTrade, bars[i + 1].open, bars[i + 1].date, 'time_stop_10d');
        openTrade = null;
      }
    }

    // Look for new entry on bar i (using bar i-1 as confirmation)
    if (!openTrade && i - 1 >= 220) {
      const evalRes = evaluateSetup(bars, spyBars, i - 1, opts.marketCfg || {});
      if (evalRes.confirmation_bar && evalRes.trade_plan) {
        const trigger = evalRes.trade_plan.entry_trigger;
        const todayOpen = bars[i].open;
        if (todayOpen > trigger * 1.010) {
          // gap > 1% — skip
        } else if (high[i] >= trigger) {
          // Stop-buy fills at MAX(trigger, open) — pre-slippage
          const rawFill = Math.max(trigger, todayOpen);
          const fill = rawFill * (1 + slip); // buy-side slippage
          const stop = fill - 1.5 * (evalRes.metrics.atr14);
          const target = fill + 2 * (fill - stop);
          const riskPerShare = fill - stop;
          const dollarsAtRisk = 0.0075 * equity;
          let shares = Math.floor(dollarsAtRisk / riskPerShare);
          const cashCap = 0.25 * equity;
          if (shares * fill > cashCap) shares = Math.floor(cashCap / fill);
          if (shares > 0) {
            equity -= commission; // entry commission
            openTrade = {
              entry_date: bars[i].date, entry_idx: i,
              entry: fill, raw_entry: rawFill,
              stop, target, initial_stop: stop,
              shares, original_shares: shares,
              R_dollars_per_share: riskPerShare,
              highest_high_since_entry: high[i],
              first_target_hit: false,
              setup_metrics: evalRes.metrics,
              legs: []
            };
          }
        }
      }
    }

    // Mark-to-market: include unrealized PnL on the runner so the equity curve is honest
    let mtm = equity;
    if (openTrade) mtm += (close[i] - openTrade.entry) * openTrade.shares;
    equityCurve.push({ date: bars[i].date, equity: mtm });
  }

  // Close any dangling trade at last bar (mark-to-close, with sell costs)
  if (openTrade) {
    closeTrade(openTrade, close[endIdx], bars[endIdx].date, 'eod_unwind');
    openTrade = null;
  }

  // Metrics
  const wins = trades.filter(t => t.R_realized > 0);
  const losses = trades.filter(t => t.R_realized <= 0);
  const winRate = trades.length ? wins.length / trades.length : 0;
  const avgWinR = wins.length ? wins.reduce((s, t) => s + t.R_realized, 0) / wins.length : 0;
  const avgLossR = losses.length ? losses.reduce((s, t) => s + t.R_realized, 0) / losses.length : 0;
  const expectancy = winRate * avgWinR - (1 - winRate) * Math.abs(avgLossR);
  const grossWinR = wins.reduce((s, t) => s + t.R_realized, 0);
  const grossLossR = Math.abs(losses.reduce((s, t) => s + t.R_realized, 0));
  const profitFactor = grossLossR > 0 ? grossWinR / grossLossR : (grossWinR > 0 ? Infinity : 0);

  // Max drawdown
  let peak = equity0, maxDD = 0;
  equityCurve.forEach(p => {
    if (p.equity > peak) peak = p.equity;
    const dd = (peak - p.equity) / peak;
    if (dd > maxDD) maxDD = dd;
  });

  return {
    final_equity: equity,
    pnl: equity - equity0,
    pnl_pct: (equity - equity0) / equity0,
    trades,
    equityCurve,
    metrics: {
      n_trades: trades.length, win_rate: winRate, avg_win_R: avgWinR, avg_loss_R: avgLossR,
      expectancy_R: expectancy, profit_factor: profitFactor, max_drawdown_pct: maxDD
    }
  };
}

// ---------- Portfolio backtest ----------
// Walks day-by-day across the entire watchlist, respecting:
//   - max concurrent positions
//   - max positions per sector
//   - portfolio heat cap (sum of open initial-stop risk)
//   - regime filter (SPY trend + VIX)
//   - sector rank filter (top-3 by 20d return)
// Uses the master SPY date timeline for alignment; per-ticker bars are looked up by date.
function runPortfolioBacktest(tickerBarsMap, tickerSectors, spyBars, sectorBarsMap, vixBars, opts = {}) {
  const equity0 = opts.equity ?? 100_000;
  const slip = (opts.slippage_bps ?? 5) / 10_000;
  const commission = opts.commission ?? 1;
  const maxPositions = opts.max_positions ?? 5;
  const maxPerSector = opts.max_per_sector ?? 2;
  const heatCapPct = opts.portfolio_heat_pct ?? 3.0;
  const riskPerTrade = opts.risk_per_trade ?? 0.0075;
  const cashCapPct = opts.cash_cap_pct ?? 0.25;
  const chandMult = opts.chandelier_atr_mult ?? 2.5;
  const startIdxOpt = opts.startIdx ?? 220;

  if (!spyBars || spyBars.length < 250) throw new Error('Portfolio backtest needs SPY history (run REFRESH first).');

  const dates = spyBars.map(b => b.date);
  const startIdx = Math.max(220, startIdxOpt);
  const endIdx = dates.length - 1;

  // Build per-ticker date→idx maps + pre-compute ATR series (for chandelier trail)
  const tickerIdx = {};
  for (const [t, bars] of Object.entries(tickerBarsMap)) {
    if (!bars || bars.error || bars.length < 220) continue;
    const m = new Map();
    bars.forEach((b, i) => m.set(b.date, i));
    const atrSeries = atr(bars.map(b=>b.high), bars.map(b=>b.low), bars.map(b=>b.close), 14);
    tickerIdx[t] = { bars, dateMap: m, atrSeries };
  }
  const sectorIdx = {};
  for (const [s, bars] of Object.entries(sectorBarsMap || {})) {
    if (!bars || bars.error) continue;
    const m = new Map();
    bars.forEach((b, i) => m.set(b.date, i));
    sectorIdx[s] = { bars, dateMap: m };
  }
  const vixMap = new Map();
  if (vixBars) vixBars.forEach((b, i) => vixMap.set(b.date, i));

  const spyClose = spyBars.map(b => b.close);
  const spy200 = sma(spyClose, 200);
  const spy50  = sma(spyClose,  50);

  let equity = equity0;
  const openTrades = [];
  const closedTrades = [];
  const equityCurve = [{ date: dates[startIdx], equity: equity0 }];
  const skipLog = [];

  function realizedR(t) {
    return t.legs.reduce((s, leg) =>
      s + ((leg.fill_price - t.entry) / t.R_dollars_per_share) * (leg.shares / t.original_shares), 0);
  }
  function closePosition(t, fillPx, exitDate, reason) {
    const fp = fillPx * (1 - slip);
    equity += (fp - t.entry) * t.shares - commission;
    t.legs.push({ date: exitDate, shares: t.shares, fill_price: fp });
    t.exit_date = exitDate; t.exit_price = fp; t.exit_reason = reason;
    t.shares_remaining_at_exit = t.shares;
    t.R_realized = realizedR(t);
    closedTrades.push(t);
  }

  for (let di = startIdx; di <= endIdx; di++) {
    const today = dates[di];

    // --- 1) Manage open positions
    for (let p = openTrades.length - 1; p >= 0; p--) {
      const t = openTrades[p];
      const info = tickerIdx[t.ticker];
      const tIdx = info.dateMap.get(today);
      if (tIdx == null) continue; // ticker didn't trade today (halt) — hold
      const bar = info.bars[tIdx];
      if (bar.high > t.highest_high_since_entry) t.highest_high_since_entry = bar.high;

      // Chandelier ratchet (only after first target hit)
      if (t.first_target_hit) {
        const a = info.atrSeries[tIdx];
        if (a) {
          const trail = t.highest_high_since_entry - chandMult * a;
          if (trail > t.stop) t.stop = trail;
        }
      }
      // Stop hit?
      if (bar.low <= t.stop) {
        const rawExit = Math.min(t.stop, bar.open);
        const reason = t.first_target_hit ? 'trail_stop' : (rawExit < t.stop ? 'gap_down_stop' : 'initial_stop');
        closePosition(t, rawExit, today, reason);
        openTrades.splice(p, 1);
        continue;
      }
      // First target?
      if (!t.first_target_hit && bar.high >= t.target) {
        const half = Math.max(1, Math.floor(t.shares / 2));
        const remaining = t.shares - half;
        const fp = t.target * (1 - slip);
        equity += (fp - t.entry) * half - commission;
        t.legs.push({ date: today, shares: half, fill_price: fp });
        t.first_target_hit = true; t.first_target_hit_date = today;
        t.shares = remaining; t.stop = t.entry; // breakeven on runner
        if (remaining === 0) {
          t.exit_date = today; t.exit_price = fp; t.exit_reason = 'first_target_full';
          t.R_realized = realizedR(t);
          closedTrades.push(t); openTrades.splice(p, 1);
          continue;
        }
      }
      // Time stop: 10 trading days
      if ((di - t.entry_di) >= 10) {
        const next = Math.min(di + 1, endIdx);
        const nextDate = dates[next];
        const nextTIdx = info.dateMap.get(nextDate);
        const fillPx = nextTIdx != null ? info.bars[nextTIdx].open : bar.close;
        closePosition(t, fillPx, nextDate, 'time_stop_10d');
        openTrades.splice(p, 1);
      }
    }

    // --- 2) Regime check (inline, cheap)
    const spyTrendOk = spyClose[di] > (spy200[di] || Infinity) && spyClose[di] > (spy50[di] || Infinity);
    const vixI = vixMap.get(today);
    const vixOk = vixI == null || vixBars[vixI].close < 25;
    const tradeable = spyTrendOk && vixOk;

    // --- 3) Look for new entries (only if tradeable + room)
    if (tradeable && openTrades.length < maxPositions) {
      // Today's sector ranks
      const sectRanks = [];
      for (const [s, sInfo] of Object.entries(sectorIdx)) {
        const sIdx = sInfo.dateMap.get(today);
        if (sIdx == null || sIdx < 20) continue;
        sectRanks.push({ s, ret: sInfo.bars[sIdx].close / sInfo.bars[sIdx - 20].close - 1 });
      }
      sectRanks.sort((a, b) => b.ret - a.ret);
      const top3 = new Set(sectRanks.slice(0, 3).map(r => r.s));

      const sectorCounts = {};
      openTrades.forEach(t => sectorCounts[t.sector] = (sectorCounts[t.sector] || 0) + 1);
      const heldTickers = new Set(openTrades.map(t => t.ticker));
      const currentHeat$ = openTrades.reduce((s, t) =>
        s + Math.max(0, (t.entry - t.stop) * t.shares), 0);

      // Find candidates with confirmed setup TODAY
      const candidates = [];
      for (const [ticker, info] of Object.entries(tickerIdx)) {
        if (heldTickers.has(ticker)) continue;
        const sec = tickerSectors[ticker];
        if (!top3.has(sec)) continue;
        if ((sectorCounts[sec] || 0) >= maxPerSector) continue;
        const tIdx = info.dateMap.get(today);
        if (tIdx == null || tIdx < 220) continue;
        const ev = evaluateSetup(info.bars, spyBars, tIdx, opts.marketCfg || {});
        if (!ev.confirmation_bar || !ev.trade_plan) continue;
        candidates.push({ ticker, sector: sec, ev, ret_1m: ev.metrics?.ret_1m ?? 0 });
      }
      candidates.sort((a, b) => b.ret_1m - a.ret_1m);

      // --- 4) Process entries on TOMORROW's open
      for (const c of candidates) {
        if (openTrades.length >= maxPositions) break;
        if ((sectorCounts[c.sector] || 0) >= maxPerSector) continue;
        if (di + 1 > endIdx) break;
        const tomorrow = dates[di + 1];
        const info = tickerIdx[c.ticker];
        const nextTIdx = info.dateMap.get(tomorrow);
        if (nextTIdx == null) continue;
        const nb = info.bars[nextTIdx];
        const trigger = c.ev.trade_plan.entry_trigger;
        if (nb.open > trigger * 1.01) { skipLog.push({ date: tomorrow, ticker: c.ticker, reason: 'gap > 1%' }); continue; }
        if (nb.high < trigger) continue; // didn't trigger
        const rawFill = Math.max(trigger, nb.open);
        const fill = rawFill * (1 + slip);
        const stop0 = fill - 1.5 * c.ev.metrics.atr14;
        const target = fill + 2 * (fill - stop0);
        const riskPS = fill - stop0;
        let shares = Math.floor((riskPerTrade * equity) / riskPS);
        const cashCap = cashCapPct * equity;
        if (shares * fill > cashCap) shares = Math.floor(cashCap / fill);
        if (shares <= 0) continue;
        // Heat check: would adding this push portfolio heat over cap?
        const newHeat$ = currentHeat$ + riskPS * shares;
        if ((newHeat$ / equity) * 100 > heatCapPct) {
          skipLog.push({ date: tomorrow, ticker: c.ticker, reason: `heat cap (${((newHeat$/equity)*100).toFixed(2)}% > ${heatCapPct}%)` });
          continue;
        }
        equity -= commission;
        openTrades.push({
          ticker: c.ticker, sector: c.sector,
          entry_date: tomorrow, entry_di: di + 1,
          entry: fill, raw_entry: rawFill,
          stop: stop0, initial_stop: stop0, target,
          shares, original_shares: shares,
          R_dollars_per_share: riskPS,
          highest_high_since_entry: nb.high,
          first_target_hit: false,
          legs: [],
          setup_metrics: c.ev.metrics
        });
        sectorCounts[c.sector] = (sectorCounts[c.sector] || 0) + 1;
      }
    }

    // --- 5) Mark to market (closed PnL + open positions at close)
    let mtm = equity;
    for (const t of openTrades) {
      const tIdx = tickerIdx[t.ticker].dateMap.get(today);
      if (tIdx != null) mtm += (tickerIdx[t.ticker].bars[tIdx].close - t.entry) * t.shares;
    }
    equityCurve.push({ date: today, equity: mtm });
  }

  // Close any dangling trades at last available bar
  for (const t of [...openTrades]) {
    const info = tickerIdx[t.ticker];
    const last = info.bars[info.bars.length - 1];
    closePosition(t, last.close, last.date, 'eod_unwind');
  }

  // Aggregate metrics
  const trades = closedTrades;
  const wins = trades.filter(t => t.R_realized > 0);
  const losses = trades.filter(t => t.R_realized <= 0);
  const winRate = trades.length ? wins.length / trades.length : 0;
  const grossW = wins.reduce((s, t) => s + t.R_realized, 0);
  const grossL = Math.abs(losses.reduce((s, t) => s + t.R_realized, 0));
  const expectancy = trades.length ? trades.reduce((s,t) => s + t.R_realized, 0) / trades.length : 0;
  const profitFactor = grossL > 0 ? grossW / grossL : (grossW > 0 ? Infinity : 0);
  let peak = equity0, maxDD = 0;
  equityCurve.forEach(p => { if (p.equity > peak) peak = p.equity; const dd = (peak - p.equity) / peak; if (dd > maxDD) maxDD = dd; });

  // Per-sector breakdown
  const bySector = {};
  trades.forEach(t => {
    const s = t.sector || 'UNK';
    if (!bySector[s]) bySector[s] = { n: 0, totalR: 0, wins: 0 };
    bySector[s].n++;
    bySector[s].totalR += t.R_realized;
    if (t.R_realized > 0) bySector[s].wins++;
  });

  // CAGR over the equity curve
  const years = (new Date(equityCurve[equityCurve.length - 1].date) - new Date(equityCurve[0].date)) / (365.25 * 86400000);
  const cagr = years > 0 ? Math.pow(equity / equity0, 1 / years) - 1 : 0;

  return {
    final_equity: equity, pnl: equity - equity0, pnl_pct: (equity - equity0) / equity0,
    trades, equityCurve, skipLog, bySector,
    metrics: {
      n_trades: trades.length, win_rate: winRate, expectancy_R: expectancy,
      profit_factor: profitFactor, max_drawdown_pct: maxDD,
      total_R: trades.reduce((s,t) => s + t.R_realized, 0),
      cagr
    }
  };
}

// ---------- Stooq CSV parser (browser fetch helper) ----------

function parseStooqCsv(text) {
  // CSV header: Date,Open,High,Low,Close,Volume
  const lines = text.trim().split(/\r?\n/);
  if (lines.length < 2) return [];
  const out = [];
  for (let i = 1; i < lines.length; i++) {
    const parts = lines[i].split(',');
    if (parts.length < 6) continue;
    const [date, open, high, low, close, volume] = parts;
    if (!date || open === '' || open === 'null') continue;
    out.push({
      date,
      open: +open, high: +high, low: +low, close: +close,
      volume: +volume
    });
  }
  return out;
}

